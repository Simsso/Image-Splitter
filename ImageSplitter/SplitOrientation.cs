namespace ImageSplitter
{

    /// <summary>
    /// A horizontal split orientation represents a cut that goes from left to right, that means the object is split into a top and a bottom part.
    /// A vertical split is a cut from top to bottom, i.e. the object is split into left and right part.
    /// </summary>
    enum SplitOrientation
    {
        Horizontal,
        Vertical
    }
}
